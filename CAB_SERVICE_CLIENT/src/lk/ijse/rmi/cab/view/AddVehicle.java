    private void getAllInsuaranceCompany() {
        try {
            List<InsuaranceCompanyDTO> insuaranceCompanyDTOs=InsuaranceCompanyController.findAllInsuaranceCompany();
            for (InsuaranceCompanyDTO insuaranceCompanyDTO : insuaranceCompanyDTOs) {
                JComboInsuaranceCompany.addItem(insuaranceCompanyDTO.getCompanyName());
                
            }
        } catch (Exception ex) {
            Logger.getLogger(AddVehicle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


    private void btnSaveVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveVehicleActionPerformed
        try {
            
            InsuaranceCompanyDTO insuaranceCompanyDTO=new InsuaranceCompanyDTO();
            insuaranceCompanyDTO.setInsuaranceCompanyId(txtInsuranceCompanyId.getText());
            insuaranceCompanyDTO.setCompanyName(JComboInsuaranceCompany.getSelectedItem().toString());
            insuaranceCompanyDTO.setInsuaranceType(txtInsuaranceType.getText());
            VehicleDTO vehicleDTO=new VehicleDTO(txtVehicleBrand.getText(),txtInsuaranceId.getText(),
                    txtRegisterNumber.getText(), txtColour.getText(),Integer.parseInt(txtSeatingCapacity.getText()),
                    txtFuelType.getText(),txtStatuc.getText(),Double.parseDouble(txtPrice.getText()),insuaranceCompanyDTO);
            
            boolean isAdded=VehicleController.addVehicle(vehicleDTO);
            if(isAdded){
                JOptionPane.showMessageDialog(this,"Added");
            }else{
                JOptionPane.showMessageDialog(this,"Not Added");
            }
        } catch (Exception ex) {
            Logger.getLogger(AddVehicle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveVehicleActionPerformed

    private void JComboInsuaranceCompanyItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_JComboInsuaranceCompanyItemStateChanged
         try {
            String companyName=JComboInsuaranceCompany.getSelectedItem().toString();
            InsuaranceCompanyDTO insuaranceCompanyDTO=InsuaranceCompanyController.getFindByCompanyName(companyName);
           //this.insuaranceCompanyDTO=insuaranceCompanyDTO;
              if(insuaranceCompanyDTO!=null){
                    txtInsuranceCompanyId.setText(insuaranceCompanyDTO.getInsuaranceCompanyId());
                    txtInsuaranceType.setText(insuaranceCompanyDTO.getInsuaranceType());
            }
        } catch (Exception ex) {
            Logger.getLogger(AddVehicle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_JComboInsuaranceCompanyItemStateChanged


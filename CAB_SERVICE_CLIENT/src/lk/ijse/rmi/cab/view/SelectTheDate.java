    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked

       
        
        Date checkin=dateStartDate.getDate();
        Date checkout=dateEnd.getDate();
        if(checkin==null || checkout==null){
            JOptionPane.showMessageDialog(null,"Please Enter Start Date  &  End Date ","Error",JOptionPane.WARNING_MESSAGE);
        }else{
            Date in=dateStartDate.getDate();
            SimpleDateFormat df=new SimpleDateFormat("yyyy-MM-dd");
            String formateDateIn=df.format(in);

            Date out=dateEnd.getDate();
            SimpleDateFormat dfe=new SimpleDateFormat("yyyy-MM-dd");
            String formatDateOut=dfe.format(out);
            
        SerchAvailableVehicle serchAvailableVehicle=new SerchAvailableVehicle(formateDateIn,formatDateOut);
        
        serchAvailableVehicle.setSize(this.getSize());
        this.removeAll();
        this.add(serchAvailableVehicle);
        this.repaint();
        this.revalidate();
        }


    }//GEN-LAST:event_btnSearchMouseClicked


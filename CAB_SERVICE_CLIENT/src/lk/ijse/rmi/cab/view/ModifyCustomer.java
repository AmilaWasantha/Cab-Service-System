    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            CustomerDTO customerDTO=new CustomerDTO();
            customerDTO.setCustId(Integer.parseInt(txtCustomerId.getText()));
            customerDTO.setCustName(txtCustName.getText());
            customerDTO.setCustNic(txtCustNicNumber.getText());
            customerDTO.setCustLicenseId(txtCustLicenceId.getText());
            customerDTO.setCustTelNumber(txtCustTelNum.getText());
            customerDTO.setCustDob(txtCustDOB.getText());
            
            boolean isUpdate=CustomerController.updateCustomer(customerDTO);
            if(isUpdate){
                CustomerController.customerReleaseByNicNumber(txtCustNicNumber.getText());
                JOptionPane.showMessageDialog(this,"customer has been update successfully");
            }else{
                JOptionPane.showMessageDialog(this,"This Customer Is Reseved OR Not Update");
            }
        } catch (Exception ex) {
            Logger.getLogger(ModifyCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            int custId=Integer.parseInt(txtCustomerId.getText());
            boolean isDelete=CustomerController.deleteCustomer(custId);
            if(isDelete){
                CustomerController.customerReleaseByNicNumber(txtCustNicNumber.getText());
                JOptionPane.showMessageDialog(this,"customer has been delete successfully");
            }else{
                JOptionPane.showMessageDialog(this,"This Customer Is Recived OR Not Delete");
            }
        } catch (Exception ex) {
            Logger.getLogger(ModifyCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


    private void loadAllCustomers() {
        try {
            DefaultTableModel dtm=(DefaultTableModel) tableCustomer.getModel();
            dtm.setRowCount(0);
            
            List<CustomerDTO>customerDTOs=CustomerController.findAllCustomers();
            
            for (CustomerDTO customerDTO : customerDTOs) {
                Object[]rowData={
                    customerDTO.getCustId(),
                    customerDTO.getCustName(),
                    customerDTO.getCustNic(),
                    customerDTO.getCustLicenseId(),
                    customerDTO.getCustTelNumber(),
                    customerDTO.getCustDob()
                };
                dtm.addRow(rowData);
                
            }
        } catch (Exception ex) {
            Logger.getLogger(ModifyCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    @Override
    public void update(String message) throws Exception {
       JOptionPane.showMessageDialog(this, message);
       loadAllCustomers();
    }

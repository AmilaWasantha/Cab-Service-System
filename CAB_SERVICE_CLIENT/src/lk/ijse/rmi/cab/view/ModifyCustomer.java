    private void txtCustNicNumberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCustNicNumberActionPerformed
        
            
        try {
            String nicNumber=txtCustNicNumber.getText();
            CustomerDTO customerDTO=CustomerController.findBYNicNumber(nicNumber);
            if(customerDTO!=null){
                boolean reserved= CustomerController.customerReseveByNicNumber(nicNumber);
                if(reserved){ 
                    txtCustomerId.setText(customerDTO.getCustId()+"");
                    txtCustName.setText(customerDTO.getCustName());
                    txtCustLicenceId.setText(customerDTO.getCustLicenseId());
                    txtCustDOB.setText(customerDTO.getCustDob());
                    txtCustTelNum.setText(customerDTO.getCustTelNumber());
                }else{
                    JOptionPane.showMessageDialog(this,"This Id Is While Reserving");
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(ModifyCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
                

            
        
    }//GEN-LAST:event_txtCustNicNumberActionPerformed

